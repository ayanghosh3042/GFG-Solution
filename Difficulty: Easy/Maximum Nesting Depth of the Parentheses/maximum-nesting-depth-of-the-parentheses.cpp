class Solution {
  public:
    int maxDepth(string s) {
        int count=0,max=0;
        for( auto i : s){
            if(i=='(')count++;
            else if(i==')')count--;
            max=(count>max)?count:max;
        }
        return max;
    }
};

//{ Driver Code Starts.
int main() {
    int t;
    cin >> t;
    while (t--) {
        string s;
        cin >> s;

        Solution obj;
        int ans = obj.maxDepth(s);
        cout << ans << endl;
    }
    return 0;
}
