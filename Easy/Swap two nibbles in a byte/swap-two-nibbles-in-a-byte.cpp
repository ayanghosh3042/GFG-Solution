class Solution {
  public:
    int swapNibbles(int n) {
        int n1=n%16;
        int n2=n/16;
        
        return n1*16+n2;
    }
};

//{ Driver Code Starts.
int main() {
    int t;
    cin >> t;
    while (t--) {
        int n;
        cin >> n;
        Solution ob;
        cout << ob.swapNibbles(n) << endl;
    }
    return 0;
}
